                    <p class="article-body">To survey the health-seeking behaviors and perspectives of the Egyptian
                        population toward the COVID-19 pandemic. A descriptive survey was designed and disseminated via
                        social media platforms. The survey consisted of 32 questions addressing respondent's
                        demographics, knowledge, practice, and attitude toward the COVID-19 pandemic. A total of 25,994
                        Egyptians participated in the survey from the 29 Egyptian governorates. More than 99% of the
                        respondents were aware of the COVID-19 pandemic. Responses showed split opinions regarding
                        whether people should wear gloves or masks to prevent COVID-19 infection (47.7% and 49.5%
                        replied with "False", respectively). Almost one-quarter (23.1%) of the respondents went to
                        crowded places during the last 14 days. Calling the emergency hotline and self-isolation at home
                        were the most frequent practices to deal with COVID-19 symptoms (34.1% and 44.5%, respectively).
                        A total of 85% of respondents reported their confidence in the Egyptian healthcare system to win
                        the battle against COVID-19 despite the challenges. A vast majority of this large population
