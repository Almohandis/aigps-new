                    <h2 class="abstract"><b>Abstract</b></h2>
                    <p class="article-body"> Pandemics are large-scale outbreaks of infectious disease that can
                        greatly increase morbidity and mortality over a wide geographic area and cause significant
                        economic, social, and political disruption. Evidence suggests that the likelihood of pandemics
                        has increased over the past century because of increased global travel and integration,
                        urbanization, changes in land use, and greater exploitation of the natural environment (Jones
                        and others 2008; Morse 1995). These trends likely will continue and will intensify. Significant
                        policy attention has focused on the need to identify and limit emerging outbreaks that might
                        lead to pandemics and to expand and sustain investment to build preparedness and health capacity
                        (Smolinsky, Hamburg, and Lederberg 2003).

                        The international community has made progress toward preparing for and mitigating the impacts of
                        pandemics. The 2003 severe acute respiratory syndrome (SARS) pandemic and growing concerns about
                        the threat posed by avian influenza led many countries to devise pandemic plans (U.S. Department
                        of Health and Human Services 2005). Delayed reporting of early SARS cases also led the World
                        Health Assembly to update the International Health Regulations (IHR) to compel all World Health
                        Organization member states to meet specific standards for detecting, reporting on, and
                        responding to outbreaks (WHO 2005). <br><a style="color:red;">To read the full article
