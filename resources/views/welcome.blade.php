                    <h2 class="abstract"><b>Abstract</b></h2>
                    <p class="article-body">The COVID-19 pandemic has turned the health care system upside down and
                        challenged consumers’ sense of well-being. In many ways, consumers are taking charge of their
                        health more than ever before. They are learning about their health risks, communicating with
                        their doctors in new and different ways, and changing their attitudes about data privacy. Each
                        of these factors has a significant influence on how patients are interacting with the health
                        system. Going forward, how will these events and factors change their behavior? Are we more or
                        less likely to see empowered health care consumers? The Deloitte “2020 Survey of U.S. Health
                        Care Consumers” provides insights into current U.S. consumer behaviors and attitudes. Since
                        2008, Deloitte has been conducting this biennial survey to explore and collect longitudinal data
                        on the subject. This year’s survey was fielded just before the pandemic started; additional
                        research, the “Health Care Consumer Response to COVID-19 Survey,” collected insights in April
                        and early May.

                        Findings show that:

                        <b>Many consumers are demonstrating agency and engagement</b>. Patients increasingly are willing
                        to tell their doctors when they disagree with them and are using tools to get information on
